
/* <--------------Card-1---------------> */
var textbox1 = document.getElementsByClassName('textbox1');
var submit_btn1 = document.getElementsByClassName('submit1');
var namewrapper1 = document.getElementsByClassName('name-wrapper1');
var para1 = document.getElementsByClassName('para1');
var add_comment1 = document.getElementsByClassName('comment1');
var inner_label1 = document.getElementsByClassName("inner_label1");


function click1() {
    
    textbox1[0].style.display = "block";
    submit_btn1[0].style.display = "block";
    add_comment1[0].style.display = "none";

    console.log("Hello ");

}
function submit1() {
    console.log("Bye");
    inner_label1[0].innerText = textbox1[0].value;
    textbox1[0].style.display = "none";
    submit_btn1[0].style.display = "none";
    para1[0].style.display = "block";
    namewrapper1[0].style.display = "block";

    
}
